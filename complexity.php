		<h6>What is complexity?</h6>
		<p>Well, that's a bit of a difficult question to answer - but that's partly what makes it fun! It has been said that there as many definitions of complex systems as there are complex systems themselves, so I've just picked a few of the definitions that I've seen and listed them below.</p>
		<p><em>A system whose properties are not fully understood by an understanding of its component parts.</em> (Science special issue, 1999)</p>
		<p><em>A complex system is one whose evolution is very sensitive to initial conditions or to small perturbations, one in which the number of independent interacting components is large, or one in which there are multiple pathways by which the system can evolve.</em> (Whitesides and Ismagilov, 1999, from a <a href="http://www.uvm.edu/~pdodds/research/papers/others/1999/whitesides1999.pdf">paper about complexity in chemistry</a>)</p>
		<p><em>A complex system is one that, by design or function, or both, is difficult to understand and to verify.</em> (Weng, Bhalla and Iyengar, 2002 from a <a href="http://www.uvm.edu/~cems/complexsystems/newsevents/pdfs/2007/weng1999.pdf">paper about complexity in biological signalling systems</a>)</p>
		<p><em>A complex system is one in which there are multiple interactions between many different components.</em> (Rind, 1999, from a <a href="https://www.uvm.edu/~cems/complexsystems/newsevents/pdfs/2007/rind1999.pdf">paper about complexity and climate</a>)</p>
